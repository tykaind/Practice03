package com.javaex.practice;

public class Ex04 {

	public static void main(String[] args) {
			//1 2 3 4세로로 출력된다.
			for(int i=1; i<5; i++) {
				System.out.println(i);
			}
			
			System.out.println("=================");
			// 10 -2뺀 숫자들이 입력되서 2로끝난다.
			for(int i =10; i>0; i=i-2) {
				System.out.println(i);
			}

	}

}
